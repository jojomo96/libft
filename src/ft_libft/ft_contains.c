#include "libft.h"

int	ft_contains(char *str, char *set)
{
	int	i;
	int	j;

	i = -1;
	while (str[++i])
	{
		j = -1;
		while (set[++j])
		{
			if (str[i] == set[j])
				return (1);
		}
	}
	return (0);
}
